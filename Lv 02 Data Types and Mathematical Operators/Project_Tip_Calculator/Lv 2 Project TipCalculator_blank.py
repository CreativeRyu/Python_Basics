# 1. Erstellen Sie eine Begrüßung für Ihr Programm.

# 2. Fragen Sie den Benutzenden wie hoch die komplette Rechnung ist.

# 3. Fragen Sie den Benutzenden wie viel Trinkgeld (in %) gegeben werden soll.

# 4. Fragen Sie anschließend auf wie viele Personen die Rechnung aufgeteilt werden soll.

# 5. Das Programm soll nun den zu zahlenden Betrag für jede einzelne Person inklusive des Trinkgeldes berechnen.

